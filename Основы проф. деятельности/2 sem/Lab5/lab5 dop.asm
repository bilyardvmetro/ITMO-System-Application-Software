ORG 0x011
COUNTER: 	WORD 0x0000
MAX:		WORD 0x0003

START: 		LD COUNTER
		INC

		CMP #01
		BEQ PRINTSTONE
PRINTSTONE: 	PUSH
		CALL $STONE
		POP

		CMP #02
		BEQ PRINTSCISSORS
PRINTSCISSORS: 	PUSH
		CALL $SCISSORS
		POP

		CMP #03
		BEQ PRINTPAPER
PRINTPAPER:	PUSH
		CALL $PAPER
		POP

		CMP MAX
		BEQ RESET
		JUMP START

RESET:		LD#00
		ST COUNTER


